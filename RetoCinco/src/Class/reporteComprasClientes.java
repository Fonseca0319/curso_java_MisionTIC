package Class;

/**
 *
 * @author Usuario
 */
public class reporteComprasClientes {
    private double documento;
    private String nombre;
    private int canridad;


    
    
    /**
     * @return the documento
     */
    public double getDocumento() {
        return documento;
    }

    /**
     * @param documento the documento to set
     */
    public void setDocumento(double documento) {
        this.documento = documento;
    }

    /**
     * @return the nombre
     */
    public String getNombre() {
        return nombre;
    }

    /**
     * @param nombre the nombre to set
     */
    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    /**
     * @return the canridad
     */
    public int getCanridad() {
        return canridad;
    }

    /**
     * @param canridad the canridad to set
     */
    public void setCanridad(int canridad) {
        this.canridad = canridad;
    }
    
    
}
