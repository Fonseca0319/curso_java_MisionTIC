    private void btncrearEnvioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncrearEnvioActionPerformed
        String envios = txtidEnvio.getText();
        String direccion = txtdireccionFacturacion.getText();
        boolean finalizado = cbFinalizado.isSelected();
        String fechaEnvio = txtfechaEnvio.getText();
        String compania = cbcompania.getSelectedItem().toString();

        if (envios.equals("") || direccion.equals("") || fechaEnvio.equals("")) {
            JOptionPane.showMessageDialog(this, "debes llenar todos los campos");
        } else {
            Envio envioUno = new Envio(envios, direccion, finalizado, fechaEnvio, compania);
            EnvioObjetoLista.add(envioUno);
            this.listEnvio();
            JOptionPane.showMessageDialog(this, "Registro Exitoso");
        }
    }

